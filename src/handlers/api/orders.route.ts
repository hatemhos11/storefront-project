import express, { NextFunction, Request, RequestHandler, Response, Router } from 'express'
import { orderType } from '../../interfaces/order.interface'
import ordersClass from '../../models/orders.model'

const router: Router = express.Router()
//                      GET ALL Product
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
router.get('/', async (_req: Request, res: Response, next: NextFunction) => {
    try {
        const orders = await ordersClass.Get()
        res.json({ ...orders })
    } catch (err) {
        next(err)
    }
})
//                      GET Specific Product
router.get('/:orderID', async (req: Request, res: Response, next: NextFunction) => {
    try {
        const { orderID } = req.params
        const { user_id } = req.body
        const order: orderType[] = await ordersClass.GetOne(orderID, user_id)
        res.json({ ...order })
    } catch (err) {
        next(err)
    }
})
// ↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑
// ↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑

//                      ADD New Product
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
router.post('/add', async (req: Request, res: Response, next: NextFunction) => {
    try {
        const { userID, status } = req.body
        const data: orderType[] = await ordersClass.Insert(userID, status)
        res.json({ ...data })
    } catch (err) {
        next(err)
    }
})
// ↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑
