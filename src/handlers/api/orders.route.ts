//                      DELETE Product
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
router.delete('/:id', async (req: Request, res: Response, next: NextFunction) => {
    try {
        const id: string = req.params.id
        const userID: string = req.body.userID

        const order: orderType[] = await ordersClass.delete(id, userID)
        res.json({ ...order, message: 'The Order Has Deleted' })
    } catch (err) {
        next(err)
    }
})
