router.delete('/:userID', CheckToken, async (req: Request, res: Response, next: NextFunction) => {
    try {
        const { userID } = req.params
        const user: userType[] = await usersClass.delete(userID)
        res.json({ user, message: 'The user Has Deleted' })
    } catch (err) {
        next(err)
    }
})
