//                      ADD New Product
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
router.post('/:orderID/add-product/:productID', async (req: Request, res: Response, next: NextFunction) => {
    const orderID: number = parseInt(req.params.orderID as string)
    const productID: number = parseInt(req.params.productID as string)
    const quantity: number = parseInt(req.body as string)
    try {
        const product: orderType[] = await productsOrderClass.Insert(
            productID,
            orderID,
            quantity
        )
        res.json({ ...product })
    } catch (err) {
        next(err)
    }
})
// ↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑
