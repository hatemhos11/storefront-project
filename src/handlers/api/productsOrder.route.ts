import express, { NextFunction, Request, RequestHandler, Response, Router } from 'express'
import { orderType } from '../../interfaces/order.interface'
import productsOrderClass from '../../models/productsOrder.model'

const router: Router = express.Router()

//                      GET ALL Product
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
// ↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓
router.get('/', async (_req: Request, res: Response, next: NextFunction) => {
    try {
        const products: orderType[] = await productsOrderClass.Get()
        res.json({ ...products })
    } catch (err) {
        next(err)
    }
})
//                      GET Specific Product
router.get('/:orderID/products/:productID', async (req: Request, res: Response, next: NextFunction) => {
    try {
        const orderID = parseInt(req.params.orderID as string)
        const productID = parseInt(req.params.productID as string)
        const products: orderType[] = await productsOrderClass.GetOne(orderID, productID)
        res.json({ ...products })
    } catch (err) {
        next(err)
    }
})
