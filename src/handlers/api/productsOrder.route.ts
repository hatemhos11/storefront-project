router.delete('/:orderID/products/:productID', async (req: Request, res: Response, next: NextFunction) => {
    try {
        const orderID: number = parseInt(req.params.orderID as string)
        const productID: number = parseInt(req.params.productID as string)

        const order: orderType[] = await productsOrderClass.delete(orderID, productID)
        res.json({ ...order, message: 'The Order Has Deleted' })
    } catch (err) {
        next(err)
    }
})
// ↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑↑
